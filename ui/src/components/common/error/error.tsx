"use client";
import Box from "@mui/material/Box";
import Image from "next/image";
import Typography from "@mui/material/Typography";
import { useSwrWithTrigger } from "@/hooks/swr-hooks";
import { Preference, ThemePreference } from "preferences";
import { Button } from "@/components/core/button";
import { useRouter } from "next/navigation";
import { PageError } from "@/lib/exceptions";

interface ErrorPageProps {
  error?: PageError;
}

interface ErrorProps extends ErrorPageProps {
  errorCode: number;
  message: string;
  detail: string;
  children?: React.ReactNode;
}

export function Error(props: ErrorProps) {
  const router = useRouter();
  const { data } = useSwrWithTrigger<
    Preference<ThemePreference>,
    ThemePreference
  >("/preferences/theme", { handleSuccess: false });
  const darkMode = data?.value.theme === "dark";

  return (
    <Box
      sx={{
        backgroundColor: darkMode ? "background.paper" : "background.default",
        display: "flex",
        flexDirection: "column",
        alignItems: "center",
        justifyContent: "center",
        height: "100vh",
      }}
    >
      <Box
        sx={{
          position: "relative",
          bottom: "10rem",
        }}
      >
        <Image
          src={`/images/${darkMode ? "logo" : "logo_dark"}.png`}
          alt="Foundary"
          width={140}
          height={15}
        />
      </Box>
      <Box
        sx={{
          display: "flex",
          gap: "0.25rem",
          flexDirection: "column",
          justifyContent: "center",
          alignItems: "center",
        }}
      >
        {props.children}
        <Typography
          variant="h1"
          sx={{
            color: darkMode ? "white" : "text.primary",
            fontWeight: "bold",
            fontSize: "12rem",
          }}
        >
          {props.errorCode}
        </Typography>
        <Box
          sx={{
            display: "flex",
            flexDirection: "column",
            alignItems: "center",
            padding: "0.625rem",
          }}
        >
          <Typography
            variant="body1"
            sx={{
              color: darkMode ? "white" : "text.primary",
              fontSize: "1.375rem",
            }}
          >
            {props.message}
          </Typography>
          <Typography
            variant="body2"
            sx={{
              color: "text.secondary",
              fontSize: "1.125rem",
            }}
          >
            {props.detail}
          </Typography>
        </Box>
        <Button
          title={props.error?.errorActionOptions?.actionText || "Go Back"}
          sx={{
            width: "15.5rem",
            height: "2rem",
            fontWeight: "normal",
            borderRadius: "2.5rem",
          }}
          onClick={() => {
            if (props.error?.errorActionOptions?.actionHandler) {
              props.error?.errorActionOptions.actionHandler();
            } else {
              router.back();
            }
          }}
        />
      </Box>
    </Box>
  );
}

